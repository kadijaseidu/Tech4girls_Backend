#!/usr/bin/python3
"""Writing a string that demostrates the upper(), lower(), replace(), and join() string method"""

"""Demostrating the upper() method"""
message = 'Hello, I am Khadijah Seidu, and I am a beginner in coding'
print(message.upper())

"""Demostrating the lower() method"""
sentence = 'Alice loves to read novels'
print(sentence.lower()) 

"""Demostrating the replace() method"""
text = 'Davina loves traveling'
print(text.replace('traveling', 'adventure'))

"""Demostrating the join() method"""
characters = ["B", "a", "c", "k", "e", "n", "d"]
join_characters = "".join(characters)
print(join_characters)